/**
 * Utility functions for generating and handling URL slugs
 */

export const generateSlug = (title: string): string => {
  return title
    .toLowerCase()
    .trim()
    .replace(/[^\w\s-]/g, '') // Remove special characters
    .replace(/[\s_-]+/g, '-') // Replace spaces and underscores with hyphens
    .replace(/^-+|-+$/g, ''); // Remove leading/trailing hyphens
};

export const isUUID = (str: string): boolean => {
  const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i;
  return uuidRegex.test(str);
};

export const getTripUrl = (trip: { slug?: string; id: string; title: string }): string => {
  // Use slug if available, otherwise generate from title
  const slug = trip.slug || generateSlug(trip.title);
  return `/trip/${slug}`;
};
